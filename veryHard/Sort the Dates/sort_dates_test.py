import unittest
from sort_dates import sort_dates


class TestSimplify(unittest.TestCase):
    def test_sort_dates(self):
        dates1 = [
            '18-10-2016_12:09', '01-12-2017_20:32', '18-10-2016_12:04',
            '19-10-2017_16:20', '18-10-2017_16:19', '18-10-2016_16:19'
        ]

        self.assertEqual(sort_dates(dates1, 'ASC'), [
            '18-10-2016_12:04',
            '18-10-2016_12:09',
            '18-10-2016_16:19',
            '18-10-2017_16:19',
            '19-10-2017_16:20',
            '01-12-2017_20:32',
        ])

        self.assertEqual(sort_dates(dates1, 'DSC'), [
            '01-12-2017_20:32',
            '19-10-2017_16:20',
            '18-10-2017_16:19',
            '18-10-2016_16:19',
            '18-10-2016_12:09',
            '18-10-2016_12:04'
        ])

        dates2 = [
            '11-02-2011_00:01', '21-04-2013_11:43', '02-09-2018_11:00',
            '02-09-2018_10:00', '02-09-2018_10:30', '11-02-2011_00:00'
        ]

        self.assertEqual(sort_dates(dates2, 'DSC'), [
            '02-09-2018_11:00',
            '02-09-2018_10:30',
            '02-09-2018_10:00',
            '21-04-2013_11:43',
            '11-02-2011_00:01',
            '11-02-2011_00:00'
    ])

        self.assertEqual(sort_dates(dates2, 'ASC'), [
            '11-02-2011_00:00',
            '11-02-2011_00:01',
            '21-04-2013_11:43',
            '02-09-2018_10:00',
            '02-09-2018_10:30',
            '02-09-2018_11:00'
        ])

        dates3 = [
            '03-04-1982_12:11', '09-08-2010_09:11', '14-12-2004_10:32',
            '08-12-2004_08:00', '08-12-2004_08:45', '12-02-1985_00:58'
        ]

        self.assertEqual(sort_dates(dates3, 'ASC'), [
            '03-04-1982_12:11',
            '12-02-1985_00:58',
            '08-12-2004_08:00',
            '08-12-2004_08:45',
            '14-12-2004_10:32',
            '09-08-2010_09:11'
        ])

        self.assertEqual(sort_dates(dates3, 'DSC'), [
            '09-08-2010_09:11',
            '14-12-2004_10:32',
            '08-12-2004_08:45',
            '08-12-2004_08:00',
            '12-02-1985_00:58',
            '03-04-1982_12:11'
        ])


if __name__ == '__main__':
    unittest.main()